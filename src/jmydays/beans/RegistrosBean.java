public class RegistrosBean {
    
    private String id_fecha;
    private String cbs;
    private String id_cbs;
    private String desc;

    /**
     * @return the fecha
     */
    public String getId_fecha() {
        return id_fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setId_fecha(String id_fecha) {
        this.id_fecha = id_fecha;
    }

    /**
     * @return the cbs
     */
    public String getCbs() {
        return cbs;
    }

    /**
     * @param cbs the cbs to set
     */
    public void setCbs(String cbs) {
        this.cbs = cbs;
    }

    /**
     * @return the id_cbs
     */
    public String getId_cbs() {
        return id_cbs;
    }

    /**
     * @param id_cbs the id_cbs to set
     */
    public void setId_cbs(String id_cbs) {
        this.id_cbs = id_cbs;
    }

    /**
     * @return the desc
     */
    public String getDesc() {
        return desc;
    }

    /**
     * @param desc the desc to set
     */
    public void setDesc(String desc) {
        this.desc = desc;
    }

}
